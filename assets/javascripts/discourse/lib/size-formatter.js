function formatDuration(seconds) {
  if (seconds < 60)
    return `${Math.round(seconds)} second${Math.round(seconds) !== 1 ? "s" : ""}`;
  if (seconds < 3600) {
    const m = Math.round(seconds / 60);
    return `${m} minute${m !== 1 ? "s" : ""}`;
  }
  if (seconds < 86400) {
    const h = Math.round(seconds / 3600);
    return `${h} hour${h !== 1 ? "s" : ""}`;
  }
  if (seconds < 86400 * 365) {
    const d = Math.round(seconds / 86400);
    return `${d} day${d !== 1 ? "s" : ""}`;
  }
  const y = parseFloat((seconds / (86400 * 365)).toPrecision(2));
  return `${y} year${y !== 1 ? "s" : ""}`;
}

export function getGrowthComparison(character, currentSize) {
  if (!character || !currentSize) return null;

  const c = character;
  const isMoving = c.target_offset !== c.current_offset;
  if (!isMoving) return null;

  const isGrowing = c.target_offset > c.current_offset;

  // Growth rate in percentage per day
  const ratePercent =
    (c.growth_rate_override != null ? c.growth_rate_override : 100) +
    (parseFloat(c.growth_rate_bought) || 0);

  // Instantaneous absolute growth rate in cm/day: Size * ln(1 + R/100)
  const rateCmPerDay = currentSize * Math.log(1 + ratePercent / 100.0);

  // Find closest speed comparison (log scale)
  let best = SPEED_COMPARISONS[0];
  let minDiff = Infinity;
  for (const s of SPEED_COMPARISONS) {
    const diff = Math.abs(
      Math.log10(rateCmPerDay + 1e-10) - Math.log10(s.cmPerDay + 1e-10)
    );
    if (diff < minDiff) {
      minDiff = diff;
      best = s;
    }
  }

  // How long have they been moving?
  // Use the most recent growth/shrink action's created_at — that's when this movement started.
  // 'boost_speed' actions shouldn't reset the timer.
  let durationText = "";
  const movementAction =
    Array.isArray(c.actions) &&
    c.actions.find((a) => ["grow", "shrink"].includes(a.action_type));

  if (movementAction && movementAction.created_at) {
    const elapsed =
      (Date.now() - new Date(movementAction.created_at).getTime()) / 1000;
    if (elapsed > 5) {
      durationText = ` for ${formatDuration(elapsed)}`;
    }
  }

  const verb = isGrowing ? "growing" : "shrinking";
  return `They have been ${verb}${durationText}, and their size is changing ${best.desc}.`;
}

export function getComparison(character) {
  if (!character) return "";
  const sizeCm = character.current_size;
  const name = character.name || "this character";

  // Add ranking info if available (skip for freeform)
  let rankText = "";
  if (character.character_type !== "freeform") {
    if (character.biggest_rank === 1) {
      rankText = `${name} is the biggest character on the forum and is `;
    } else if (character.tiniest_rank === 1) {
      rankText = `${name} is the tiniest character on the forum and is `;
    } else if (character.biggest_rank <= 10) {
      rankText = `${name} is the ${getOrdinal(
        character.biggest_rank
      )} biggest character on the forum and is `;
    } else if (character.tiniest_rank <= 10) {
      rankText = `${name} is the ${getOrdinal(
        character.tiniest_rank
      )} tiniest character on the forum and is `;
    }
  }

  if (!rankText) {
    rankText = `${name} is `;
  }

  let best = COMPARISONS[0];
  let minDiff = Infinity;
  for (let comp of COMPARISONS) {
    const diff = Math.abs(Math.log10(sizeCm) - Math.log10(comp.size_cm));
    if (diff < minDiff) {
      minDiff = diff;
      best = comp;
    }
  }

  let ratio = sizeCm / best.size_cm;
  let comparisonText = "";

  if (
    (ratio >= 0.95 && ratio <= 1.05) ||
    best.size_cm <= 1e-25 ||
    best.size_cm >= 1e30
  ) {
    comparisonText = best.desc;
  } else {
    let formattedRatio =
      ratio >= 100
        ? Math.round(ratio).toLocaleString()
        : parseFloat(ratio.toPrecision(2));
    comparisonText = `${formattedRatio} times ${best.desc}`;
  }

  let result = rankText + comparisonText;
  if (!result.endsWith(".") && !result.endsWith("!")) {
    result += ".";
  }
  return result;
}

export function formatSize(sizeCm, system = "metric") {
  const parsedSize = parseFloat(sizeCm);
  if (isNaN(parsedSize) || parsedSize === 0) return "0 cm";
  const absSize = Math.abs(parsedSize);

  // Shared constants
  const pm = absSize * 1e10;
  const nm = absSize * 1e7;
  const atoms = nm / 0.1; // 1 atom ≈ 0.1nm
  const cells = absSize / 0.001; // 1 cell ≈ 10µm = 0.001cm
  const lightyears = absSize / 9.461e17;
  const universes = absSize / 8.8e28;

  const sign = sizeCm < 0 ? "-" : "";

  // Absolute floor: Atoms
  if (absSize < 1e-8) {
    if (atoms < 0.001) return `${sign}0.001 atoms`;
    return `${sign}${atoms.toFixed(3)} atoms`;
  }

  if (system === "metric") {
    if (absSize < 1e-7) return `${sign}${pm.toFixed(2)} pm`;
    if (absSize < 1e-5) return `${sign}${nm.toFixed(2)} nm`;
    if (absSize < 0.01) return `${sign}${cells.toFixed(2)} cells`;
    if (absSize < 100) return `${sign}${absSize.toFixed(2)} cm`;
    if (absSize < 100000) return `${sign}${(absSize / 100).toFixed(2)} m`;
    if (absSize < 9.461e17) return `${sign}${(absSize / 100000).toFixed(2)} km`;
    if (absSize < 8.8e28) return `${sign}${lightyears.toFixed(2)} ly`;
    return `${sign}${universes.toFixed(2)} uni`;
  } else {
    if (absSize < 1e-7) return `${sign}${pm.toFixed(2)} pm`;
    if (absSize < 1e-5) return `${sign}${nm.toFixed(2)} nm`;
    if (absSize < 0.01) return `${sign}${cells.toFixed(2)} cells`;

    const inches = absSize / 2.54;
    if (inches < 12) return `${sign}${inches.toFixed(2)}"`;

    const feet = inches / 12;
    if (feet < 5280) {
      const ft = Math.floor(feet);
      const inc = Math.round((feet - ft) * 12);
      if (inc === 12) return `${sign}${ft + 1}'0"`;
      return `${sign}${ft}'${inc}"`;
    }

    const miles = feet / 5280;
    if (absSize < 9.461e17) return `${sign}${miles.toFixed(2)} mi`;
    if (absSize < 8.8e28) return `${sign}${lightyears.toFixed(2)} ly`;
    return `${sign}${universes.toFixed(2)} uni`;
  }
}
